#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	storage;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n *= -1;
		}
		if (n >= 10)
			ft_putnbr_fd((n / 10), fd);
		storage = '0' + (n % 10);
		write(fd, &storage, 1);
	}
}
